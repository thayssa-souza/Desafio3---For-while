//FOR CRESCENTE
let contagemProgressiva = 0;
for(contagemProgressiva = 1; contagemProgressiva <= 10; contagemProgressiva++){
    console.log(contagemProgressiva);
}

console.log('----------------------')

//WHILE DESCRECENTE
let contagemRegressiva = 10;
while(contagemRegressiva >= 1){
    console.log(contagemRegressiva);
    contagemRegressiva--;
}

console.log('----------------------')

